var agora = new Date()

var dia = agora.getDay()

switch (dia){
    case (dia == 0):
    console.log("Domingo")
    break

    case (dia == 1):
    console.log("Segunda")
    break

    case (dia == 2):
    console.log("Terça")
    break

    case (dia == 3):
    console.log("Quarta")
    break

    case (dia == 4):
    console.log("Quinta")
    break

    case (dia == 5):
    console.log("Sexta")
    break

    case (dia == 6):
    console.log("Sábado")
    break
}